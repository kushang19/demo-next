// for NotFound componet we do not get any params so we take help of usePathname
"use client"
import { usePathname } from "next/navigation"

export default function NotFound(){
    const pathName = usePathname();
const productId = pathName.split("/")[2]
const reviewId = pathName.split("/")[4]

    return (
        <>
            <h1>Review Page Not Found for product ID {productId} and review ID {reviewId} :( </h1>
        </>
    )
}